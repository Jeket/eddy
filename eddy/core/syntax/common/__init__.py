from abc import ABCMeta, abstractmethod

from PyQt5.QtCore import QObject


class AbstractValidator(QObject):
    """
    Base syntax validator class.
    This class defines the base structure of syntax validators and enforce the implementation
    of the 'check' method which is responsible of validation Graphol triples: NODE -> EDGE -> NODE.
    The method MUST return True if the triple is syntattically correct, False otherwise.
    """
    __metaclass__ = ABCMeta

    def __init__(self, parent=None):
        """
        Initialize the Validator.
        :type parent:  QObject
        """
        super().__init__(parent)

    ####################################################################################################################
    #                                                                                                                  #
    #   INTERFACE                                                                                                      #
    #                                                                                                                  #
    ####################################################################################################################

    @abstractmethod
    def check(self, source, edge, target):
        """
        Validate the given triple.
        Will return True if the triple is valid, False otherwise.
        :type source: AbstractNode
        :type edge: AbstractEdge
        :type target: AbstractNode
        :rtype: bool
        """
        pass
